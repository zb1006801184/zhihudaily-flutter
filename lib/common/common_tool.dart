//常用的方法
class CommonTool {
  //转换月份
  String changeMouth(int mouth) {
    switch (mouth) {
      case 1:
        return "一月";
        break;
      case 2:
        return "二月";
        break;
      case 3:
        return "三月";
        break;
      case 4:
        return "四月";
        break;
      case 5:
        return "五月";
        break;
      case 6:
        return "六月";
        break;
      case 7:
        return "七月";
        break;
      case 8:
        return "八月";
        break;
      case 9:
        return "九月";
        break;
      case 10:
        return "十月";
        break;
      case 11:
        return "十一月";
        break;
      case 12:
        return "十二月";
        break;
      default:
    }
  }
}
